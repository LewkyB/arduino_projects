void setup() {
  Serial.begin(9600);
 
  tft.begin();
 
}
 
 
void loop(void) {
  tft.setRotation(3);
  tft.fillScreen(ILI9341_BLACK);
  tft.setCursor(0, 0);
  tft.setTextColor(ILI9341_GREEN, ILI9341_RED);
  tft.setTextSize(6);

  tft.printf("%.2f F\n", thermocouple.readFahrenheit());
  Serial.printf("Thermocouple Temperature: %f\n", thermocouple.readFahrenheit());
  delay(1000);
}
 
unsigned long testFillScreen() {
  unsigned long start = micros();
  tft.fillScreen(ILI9341_BLACK);
  tft.fillScreen(ILI9341_RED);
  tft.fillScreen(ILI9341_GREEN);
  tft.fillScreen(ILI9341_BLUE);
  tft.fillScreen(ILI9341_BLACK);
  return micros() - start;
}
 
